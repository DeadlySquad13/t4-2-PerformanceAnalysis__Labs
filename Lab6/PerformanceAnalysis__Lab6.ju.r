# %% [markdown]
# ### –¢–µ–æ—Ä–µ—Ç–∏—á–µ—Å–∫–∏ –º–µ—Ç–æ–¥–æ–º —É–∫—Ä—É–ø–Ω–µ–Ω–Ω—ã—Ö —Å–æ—Å—Ç–æ—è–Ω–∏–π
# –ü—Ä–æ–∞–Ω–∞–ª–∏–∑–∏—Ä—É–µ–º —Å–ª–æ–∂–Ω—É—é —Å–∏—Å—Ç–µ–º—É –ø–æ —á–∞—Å—Ç—è–º, —Å –ø–æ–º–æ—â—å—é —É–∫—Ä—É–ø–Ω–µ–Ω–Ω—ã—Ö (–∞–≥—Ä–µ–≥–∏—Ä–æ–≤–∞–Ω–Ω—ã—Ö) –º–æ–¥–µ–ª–µ–π.
# –í –∫–∞–∂–¥–æ–π –º–æ–¥–µ–ª–∏ –ø–æ–¥—Ä–æ–±–Ω–æ –ø—Ä–µ–¥—Å—Ç–∞–≤–∏–º —Ç–æ–ª—å–∫–æ –Ω–µ–∫–æ—Ç–æ—Ä—É—é —á–∞—Å—Ç—å —Å–∏—Å—Ç–µ–º—ã, –∞ –≤–ª–∏—è–Ω–∏–µ
# –æ—Å—Ç–∞–ª—å–Ω—ã—Ö —á–∞—Å—Ç–µ–π –æ—Ç—Ä–∞–∑–∏–º –Ω–µ–∫–æ—Ç–æ—Ä—ã–º –æ–±–æ–±—â–µ–Ω–Ω—ã–º –ø–∞—Ä–∞–º–µ—Ç—Ä–æ–º (–ø–∞—Ä–∞–º–µ—Ç—Ä–æ–º —Å–≤—è–∑–∏).

# –ü—Ä–µ–¥–ª–æ–∂–∏–º –¥–µ–∫–æ–º–ø–æ–∑–∏—Ü–∏—é –Ω–∞—à–µ–π –∑–∞–¥–∞—á–∏ –≤ –≤–∏–¥–µ –¥–≤—É—Ö –º–æ–¥–µ–ª–µ–π.

# –í –ø–µ—Ä–≤–æ–π –º–æ–¥–µ–ª–∏ —Å–æ–≤–æ–∫—É–ø–Ω–æ—Å—Ç—å —Å–µ—Ä–≤–µ—Ä–æ–≤ –∏ –∫–æ–º–ø—å—é—Ç–µ—Ä–æ–≤ –ø–æ –ø—Ä–æ–≤–µ—Ä–∫–µ –Ω–∞–ª–∏—á–∏—è
# –≤–∏—Ä—É—Å–æ–≤ –∑–∞–º–µ–Ω–∏–º —Ç–æ–ª—å–∫–æ –æ–¥–Ω–∏–º –æ–±–æ–±—â–µ–Ω–Ω—ã–π –ø–∞—Ä–∞–º–µ—Ç—Ä–æ–º - –∏–Ω—Ç–µ–Ω—Å–∏–≤–Ω–æ—Å—Ç—å—é
# –æ–±—Å–ª—É–∂–∏–≤–∞–Ω–∏—è –æ–±—â $\mu_{\text{–æ–±—â}}$.

# ![Programmers graph](./Programmers_graph.png)

# –ö–∞–∫ –≤–∏–¥–Ω–æ, –º–æ–¥–µ–ª—å –±—É–¥–µ—Ç –æ–±—ã—á–Ω–æ–π –º–æ–¥–µ–ª—å—é
# —Ä–æ–∂–¥–µ–Ω–∏—è –≥–∏–±–µ–ª–∏ –¥–ª—è –∑–∞–º–∫–Ω—É—Ç–æ–π —Å–∏—Å—Ç–µ–º—ã

# %% [markdown]
# $$
# P_0 = \left(1 + \frac{K\lambda}{\mu_{\text{–æ–±—â}}} + \frac{K(K-1)\lambda^2}{\mu_{\text{–æ–±—â}}^2} + \ldots + \frac{K!\lambda^K}{\mu_{\text{–æ–±—â}}^K}\right)^{-1}
# $$

# %% [markdown]
# #### –ò—Ç–µ—Ä–∞—Ü–∏—è 1
# –ü—É—Å—Ç—å $\mu_{\text{–æ–±—â}} = 0.15 < \lambda'$. –¢–æ–≥–¥–∞:

# %%
mu_ <- 0.15
y_ <- lambda / mu_
y_

# %% [markdown]
# –ö–∞–∂–¥—ã–π —á–ª–µ–Ω —Å—É–º–º—ã –º–æ–∂–Ω–æ —Ç–∞–∫–∂–µ –ø—Ä–µ–¥—Å—Ç–∞–≤–∏—Ç—å:
# $$
# \text{sum_part}_i =\frac{K!}{(K-i)!} \cdot \frac{\lambda}{\mu_{\text{–æ–±—â}}}
# $$

# %%
i <- 0:K
sum_parts <- c(factorial(K) / factorial(K - i) * y_^i)
sum_parts

# %%
P0 <- (sum(sum_parts)
)^(-1)
P0

# %% [markdown]
# $$
# P_1 = P_0 \frac{K\lambda}{\mu_{\text{–æ–±—â}}} = P_0 \cdot \text{sum_part}_1 \\
# P_2 = P_1 \frac{(K - 1)\lambda}{\mu_{\text{–æ–±—â}}} = P_0 \cdot \text{sum_part}_2 \\
# P_3 = P_2\frac{(K - 2)\lambda}{\mu_{\text{–æ–±—â}}} = P_0 \cdot \text{sum_part}_3 \\
# P_i = P_{i-1}\frac{(K - (i - 1))\lambda}{\mu_{\text{–æ–±—â}}} = P_0 \cdot
# \text{sum_part}_i
# $$


# %%
Pi <- unlist(lapply(sum_parts, function(sum_part) P0 * sum_part))
Pi

# %% [markdown]
# –ü—Ä–æ–≤–µ—Ä–∏–º, —á—Ç–æ –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç–∏ –∏–º–µ—é—Ç –∞–¥–µ–∫–≤–∞—Ç–Ω—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è.

# %%
sum(Pi)

# %% [markdown]
# $$
# L_{\text{—Å–∏—Å—Ç}} = \sum_{k=1}^K k \cdot P_k
# $$

# %%
mean_number_of_requests1 <- sum(c(1:K) * Pi[2:(K+1)])
mean_number_of_requests1

# %% [markdown]
# –í–æ –≤—Ç–æ—Ä–æ–π –º–æ–¥–µ–ª–∏ –±—É–¥–µ–º —Å—á–∏—Ç–∞—Ç—å, —á—Ç–æ –≤ —Å–∏—Å—Ç–µ–º–µ –ø–æ—Å—Ç–æ—è–Ω–Ω–æ —Ü–∏—Ä–∫—É–ª–∏—Ä—É—é—Ç
# $L_{\text{—Å–∏—Å—Ç}}$ –∑–∞—è–≤–æ–∫. –í –∫–∞—á–µ—Å—Ç–≤–µ —Å–æ—Å—Ç–æ—è–Ω–∏–π —Å–∏—Å—Ç–µ–º—ã –≤–æ–∑—å–º–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ
# –∑–∞—è–≤–æ–∫, –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º—ã—Ö –Ω–∞ –æ—Å–Ω–æ–≤–Ω—ã—Ö —Å–µ—Ä–≤–µ—Ä–∞—Ö:
# $$
# S_0, S_1, S_2, \ldots, S_{L_{\text{—Å–∏—Å—Ç}}}
# $$

# %% [markdown]
# ![Servers graph](./Servers_graph.png)

# %% [markdown]
# –í –¥–∞–Ω–Ω–æ–π –∏—Ç–µ—Ä–∞—Ü–∏–∏, –æ–∫—Ä—É–≥–ª—è—è, –º—ã –ø–æ–ª—É—á–∏–ª–∏ $[L_{\text{—Å–∏—Å—Ç}} \approx 3.47] = 3$

# %% [markdown]
# $$
# \nu_n = (1-p)\nu \cdot \min{(N,L_{\text{—Å–∏—Å—Ç}}-n)}, n=0, \ldots, L_{\text{—Å–∏—Å—Ç}} - 1 \\
# $$

# %%
mean_number_of_requests1 <- round(mean_number_of_requests1)
n <- 0:(mean_number_of_requests1 - 1)

nu_n <- unlist(lapply(n, function(n) (1 - p) * nu * min(N, mean_number_of_requests1 - n)))
nu_n

# %% [markdown]
# $$
# \mu_k = \mu \cdot \min{(M, k)}, k=1, 2, \ldots, L_{\text{—Å–∏—Å—Ç}}
# $$

# %%
k <- 1:mean_number_of_requests1

mu_k <- unlist(lapply(k, function(k) mu * min(M, k)))
mu_k

# %% [markdown]
# –ê–Ω–∞–ª–æ–≥–∏—á–Ω—ã–º –æ–±—Ä–∞–∑–æ–º –ø–æ—Å—á–∏—Ç–∞–µ–º —Ñ–∏–Ω–∞–ª—å–Ω—ã–µ –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç–∏:
# $$
# \pi_0 = \left(1+\frac{\nu_0}{\mu_1}+\frac{\nu_0\nu_1}{\mu_1\mu_2}+\ldots\right)^{-1} \\
# \pi_1=\pi_0\cdot \frac{\nu_0}{\mu_1} \\
# \pi_2=\pi_0\cdot \frac{\nu_0\nu_1}{\mu_1\mu_2} \\
# \dots
# $$

# %%
sum_parts <- c()
sum_parts[1] <- 1
sum_parts[2] <- sum_parts[1] * nu_n[1] / mu_k[1]
sum_parts[3] <- sum_parts[2] * nu_n[2] / mu_k[2]
sum_parts[4] <- sum_parts[3] * nu_n[3] / mu_k[3]

pi_0 <- (sum(sum_parts))^(-1)
pi_0

# %%
pi_i <- unlist(lapply(sum_parts, function(sum_part) pi_0 * sum_part))
pi_i

# %% [markdown]
# –ü—Ä–æ–≤–µ—Ä–∏–º, —á—Ç–æ –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç–∏ –∏–º–µ—é—Ç –∞–¥–µ–∫–≤–∞—Ç–Ω—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è.

# %%
sum(pi_i)

# %% [markdown]
# –¢–æ–≥–¥–∞:
# $$
# \mu_{\text{–æ–±—â}} = \sum_{n=1}^{L_{\text{—Å–∏—Å—Ç}}}\pi_n\cdot \mu_n
# $$

# %%
mu_ <- sum(mu_k[1:(mean_number_of_requests1)] * pi_i[2:(mean_number_of_requests1 + 1)])
mu_

# %% [markdown]
# #### –ò—Ç–µ—Ä–∞—Ü–∏—è 2

# %%
y_ <- lambda / mu_
y_

# %% [markdown]
# –ö–∞–∂–¥—ã–π —á–ª–µ–Ω —Å—É–º–º—ã –º–æ–∂–Ω–æ —Ç–∞–∫–∂–µ –ø—Ä–µ–¥—Å—Ç–∞–≤–∏—Ç—å:
# $$
# \text{sum_part}_i =\frac{K!}{(K-i)!} \cdot \frac{\lambda}{\mu_{\text{–æ–±—â}}}
# $$

# %%
i <- 0:K
sum_parts <- c(factorial(K) / factorial(K - i) * y_^i)
sum_parts

# %%
P0 <- (sum(sum_parts)
)^(-1)
P0

# %% [markdown]
# $$
# P_1 = P_0 \frac{K\lambda}{\mu_{\text{–æ–±—â}}} = P_0 \cdot \text{sum_part}_1 \\
# P_2 = P_1 \frac{(K - 1)\lambda}{\mu_{\text{–æ–±—â}}} = P_0 \cdot \text{sum_part}_2 \\
# P_3 = P_2\frac{(K - 2)\lambda}{\mu_{\text{–æ–±—â}}} = P_0 \cdot \text{sum_part}_3 \\
# P_i = P_{i-1}\frac{(K - (i - 1))\lambda}{\mu_{\text{–æ–±—â}}} = P_0 \cdot
# \text{sum_part}_i
# $$


# %%
Pi <- unlist(lapply(sum_parts, function(sum_part) P0 * sum_part))
Pi

# %% [markdown]
# –ü—Ä–æ–≤–µ—Ä–∏–º, —á—Ç–æ –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç–∏ –∏–º–µ—é—Ç –∞–¥–µ–∫–≤–∞—Ç–Ω—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è.

# %%
sum(Pi)

# %% [markdown]
# $$
# L_{\text{—Å–∏—Å—Ç}} = \sum_{k=1}^K k \cdot P_k
# $$

# %%
mean_number_of_requests1 <- sum(c(1:K) * Pi[2:(K+1)])
mean_number_of_requests1

# %% [markdown]
# –í –¥–∞–Ω–Ω–æ–π –∏—Ç–µ—Ä–∞—Ü–∏–∏, –æ–∫—Ä—É–≥–ª—è—è, –º—ã –ø–æ–ª—É—á–∏–ª–∏ $[L_{\text{—Å–∏—Å—Ç}} \approx 3.47]
# = 3$. –Ø—Å–Ω–æ, —á—Ç–æ —Å–Ω–æ–≤–∞ –≤—ã–±—Ä–∞–≤ 3, –Ω–∏—á–µ–≥–æ –Ω–µ –ø–æ–º–µ–Ω—è–µ—Ç—Å—è, –ø–æ—ç—Ç–æ–º—É –ø–æ–ø—Ä–æ–±—É–µ–º
# $L_{\text{—Å–∏—Å—Ç}} = 2$.

# %% [markdown]
# $$
# \nu_n = (1-p)\nu \cdot \min{(N,L_{\text{—Å–∏—Å—Ç}}-n)}, n=0, \ldots, L_{\text{—Å–∏—Å—Ç}} - 1 \\
# $$

# %%
mean_number_of_requests1 <- 2
n <- 0:(mean_number_of_requests1 - 1)

nu_n <- unlist(lapply(n, function(n) (1 - p) * nu * min(N, mean_number_of_requests1 - n)))
nu_n

# %% [markdown]
# $$
# \mu_k = \mu \cdot \min{(M, k)}, k=1, 2, \ldots, L_{\text{—Å–∏—Å—Ç}}
# $$

# %%
k <- 1:mean_number_of_requests1

mu_k <- unlist(lapply(k, function(k) mu * min(M, k)))
mu_k

# %% [markdown]
# –ê–Ω–∞–ª–æ–≥–∏—á–Ω—ã–º –æ–±—Ä–∞–∑–æ–º –ø–æ—Å—á–∏—Ç–∞–µ–º —Ñ–∏–Ω–∞–ª—å–Ω—ã–µ –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç–∏:
# $$
# \pi_0 = \left(1+\frac{\nu_0}{\mu_1}+\frac{\nu_0\nu_1}{\mu_1\mu_2}+\ldots\right)^{-1} \\
# \pi_1=\pi_0\cdot \frac{\nu_0}{\mu_1} \\
# \pi_2=\pi_0\cdot \frac{\nu_0\nu_1}{\mu_1\mu_2} \\
# \dots
# $$

# %%
sum_parts <- c()
sum_parts[1] <- 1
sum_parts[2] <- sum_parts[1] * nu_n[1] / mu_k[1]
sum_parts[3] <- sum_parts[2] * nu_n[2] / mu_k[2]

pi_0 <- (sum(sum_parts))^(-1)
pi_0

# %%
pi_i <- unlist(lapply(sum_parts, function(sum_part) pi_0 * sum_part))
pi_i
# %% [markdown]
# –ü—Ä–æ–≤–µ—Ä–∏–º, —á—Ç–æ –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç–∏ –∏–º–µ—é—Ç –∞–¥–µ–∫–≤–∞—Ç–Ω—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è.

# %%
sum(pi_i)

# %% [markdown]
# –¢–æ–≥–¥–∞:
# $$
# \mu_{\text{–æ–±—â}} = \sum_{n=1}^{L_{\text{—Å–∏—Å—Ç}}}\pi_n\cdot \mu_n
# $$

# %%
mu_ <- sum(mu_k[1:(mean_number_of_requests1)] * pi_i[2:(mean_number_of_requests1 + 1)])
mu_

# %% [markdown]
# $$
# T_{\text{—Å–∏—Å—Ç}} = \frac{L_{\text{—Å–∏—Å—Ç}}}{\lambda(K-L_{\text{—Å–∏—Å—Ç}})}
# $$

# %%
T <- mean_number_of_requests1 / (lambda * (K - mean_number_of_requests1))
T

