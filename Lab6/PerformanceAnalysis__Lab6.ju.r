                in_system <- sum(on_server) + sum(in_queue)

                number_of_free_programmers <- K - in_system

                return(number_of_free_programmers)
            }) %>%
            join(programmers_source) %>%
            join(main_server) %>%
            set_global("number_of_free_programmers", value = 1, mod = "+") %>%
            join(programmers_source)

    return(env %>%
        add_resource(
            "antivirus_server",
            capacity = N,
            queue_size = m1
        ) %>%
        add_resource(
            "server",
            capacity = M,
            queue_size = m2
